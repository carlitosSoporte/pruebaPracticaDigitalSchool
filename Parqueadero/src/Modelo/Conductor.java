
package Modelo;

public class Conductor {
    
    private int idConductor;
    private int identificacionConductor;
    private String nombreConductor;
    private String apellidoConductor;
    private long telefonoConductor;
    private String emailConductor;

    public Conductor() {
    }

    public int getIdConductor() {
        return idConductor;
    }

    public void setIdConductor(int idConductor) {
        this.idConductor = idConductor;
    }

    public int getIdentificacionConductor() {
        return identificacionConductor;
    }

    public void setIdentificacionConductor(int identificacionConductor) {
        this.identificacionConductor = identificacionConductor;
    }

    public String getNombreConductor() {
        return nombreConductor;
    }

    public void setNombreConductor(String nombreConductor) {
        this.nombreConductor = nombreConductor;
    }

    public String getApellidoConductor() {
        return apellidoConductor;
    }

    public void setApellidoConductor(String apellidoConductor) {
        this.apellidoConductor = apellidoConductor;
    }

    public long getTelefonoConductor() {
        return telefonoConductor;
    }

    public void setTelefonoConductor(long telefonoConductor) {
        this.telefonoConductor = telefonoConductor;
    }

    public String getEmailConductor() {
        return emailConductor;
    }

    public void setEmailConductor(String emailConductor) {
        this.emailConductor = emailConductor;
    }
    
    
}
